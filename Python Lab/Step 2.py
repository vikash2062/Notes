# Integer 
num1 = 10 
print(num1, "is of type", type(num1)) 
# Float 
num2 = 10.5 
print(num2, "is of type", type(num2)) 
# String 
text = "Hello, Python!" 
print(text, "is of type", type(text)) 
# Boolean 
flag = True 
print(flag, "is of type", type(flag)) 
# List (ordered, mutable) 
fruits = ["Apple", "Banana", "Cherry"] 
print(fruits, "is of type", type(fruits)) 
# Tuple (ordered, immutable) 
coordinates = (10, 20) 
print(coordinates, "is of type", type(coordinates)) 
# Dictionary (key-value pairs) 
student = {"name": "Alice", "age": 25, "grade": "A"} 
print(student, "is of type", type(student)) 
# Set (unordered, unique values) 
unique_numbers = {1, 2, 3, 3, 4} 
print(unique_numbers, "is of type", type(unique_numbers)) 