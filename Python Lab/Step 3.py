a = 10 
b = 3 
# Addition 
print("Addition:", a + b) 
# Subtraction 
print("Subtraction:", a - b) 
# Multiplication 
print("Multiplication:", a * b) 
# Division 
print("Division:", a / b) 
# Floor Division (removes decimal part) 
print("Floor Division:", a // b) 
# Modulus (remainder) 
print("Modulus:", a % b) 
# Exponentiation 
print("Exponentiation:", a ** b) 