# Q 1: Declare two integer variables, perform all arithmetic operations, and print the results.


# Declare two integer variables
a = 10
b = 3

# Perform arithmetic operations
addition = a + b
subtraction = a - b
multiplication = a * b
division = a / b          # Regular division (float result)
floor_division = a // b   # Floor division (integer result)
modulus = a % b
exponentiation = a ** b

# Print the results
print("a =", a)
print("b =", b)
print("Addition (a + b) =", addition)
print("Subtraction (a - b) =", subtraction)
print("Multiplication (a * b) =", multiplication)
print("Division (a / b) =", division)
print("Floor Division (a // b) =", floor_division)
print("Modulus (a % b) =", modulus)
print("Exponentiation (a ** b) =", exponentiation)
