# Converting an integer to a string 
num = 100 
str_num = str(num) 
print(str_num, "is of type", type(str_num)) 
# Converting a string to an integer 
str_val = "50" 
int_val = int(str_val) 
print(int_val, "is of type", type(int_val)) 
# Converting a float to an integer 
float_val = 10.99 
int_from_float = int(float_val) 
print(int_from_float, "is of type", type(int_from_float)) 
# Converting a number to a boolean 
bool_val = bool(0)  # False because 0 is considered False 
print(bool_val) 